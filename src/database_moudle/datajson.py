def get_static_data(static_data):
 import os
 import copy
 new_static_data = []
 for each_data in static_data:
  data_path = each_data["data_path"]
  try:
   folders = os.listdir(data_path)
   for each_folder in folders:
    if not each_folder.endswith("jpg") or each_folder.endswith("bmp"):
     new_data_path = os.path.join(data_path,each_folder)
     new_each_data = copy.deepcopy(each_data)
     new_each_data["data_path"] = new_data_path
     new_static_data.append( new_each_data)
  except:
   continue
